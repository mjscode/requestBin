    public function update(Request $request, $id)
    {
        $sr=new storeRequest;

        $sr->url_id=$id;
        $sr->IP_Address=$request->ip();
        $sr->method=$request->method();
        //$req->headers=implode( $request->header('content-type'), ',');
       // var_dump($request->header());
